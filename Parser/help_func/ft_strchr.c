#include "../../minishell.h"

char	*ft_strchr(char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
			return ((char *) &s[i]);
		i++;
	}
	if (!(char)c)
		return ((char *) &s[i]);
	return (0);
}
